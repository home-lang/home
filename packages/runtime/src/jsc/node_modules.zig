// Phase 3 — a minimal CommonJS `require()` + a first set of `node:*` built-in
// modules for the native JSC eval/run realm.
//
// The realm evaluates scripts via `JSEvaluateScript` (script mode), so the
// loader exposed here is CommonJS `require()` (ESM `import` needs JSC's module
// loader, which is parked). `require(spec)` resolves Node built-ins, accepting
// both the `node:` prefix and the bare name:
//   - `node:path` — full POSIX path surface (pure JS, behaviorally faithful).
//   - `node:fs`   — sync `readFileSync`/`writeFileSync`/`existsSync`/`statSync`
//     /`mkdirSync`/`rmSync`, backed by Home's real native fs.
//   - `node:os`   — `platform`/`arch`/`EOL`/`homedir`/`tmpdir`/`cpus`/… derived
//     from `process` + native helpers.
//   - `node:child_process` — `spawnSync`/`execSync`/`execFileSync`/`exec`/
//     `execFile`/`spawn`, mapped onto Home's native `Bun.spawnSync` (eager v1).
//
// (Also serves events/util/buffer/assert/querystring/crypto.)
//
// Unknown specifiers throw `Error` with `code: 'ERR_MODULE_NOT_FOUND'`, matching
// Node's shape. comptime-gated on `enable_jsc`; installed after `process` +
// the web globals it relies on.

