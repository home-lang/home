/// `__home_node_readdir(path)` -> array of entry names, or null on error.
fn readdirNative(ctx: ?*JSContextRef, function: ?*JSObject, this_object: ?*JSObject, argc: usize, argv: [*c]const ?*JSValue, exception: extern_fns.ExceptionRef) callconv(.c) ?*JSValue {
    _ = function;
    _ = this_object;
    _ = exception;
    const c = ctx orelse return null;
    if (argc < 1) return extern_fns.JSValueMakeNull(c);
    const allocator = std.heap.page_allocator;
    const path = argToOwnedUtf8(c, argv[0].?, allocator) orelse return extern_fns.JSValueMakeNull(c);
    defer allocator.free(path);
    var dir = std.Io.Dir.cwd().openDir(io(), path, .{ .iterate = true }) catch return extern_fns.JSValueMakeNull(c);
    defer dir.close(io());
    var arena_inst = std.heap.ArenaAllocator.init(allocator);
    defer arena_inst.deinit();
    const a = arena_inst.allocator();
    var items: std.ArrayListUnmanaged(?*JSValue) = .empty;
    var it = dir.iterate();
    while (it.next(io()) catch null) |entry| {
        items.append(a, makeNodeString(c, entry.name)) catch break;
    }
    const arr = extern_fns.JSObjectMakeArray(c, items.items.len, if (items.items.len > 0) items.items.ptr else null, null) orelse
        return extern_fns.JSValueMakeNull(c);
    return @ptrCast(arr);
}

