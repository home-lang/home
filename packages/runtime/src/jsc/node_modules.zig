/// Install `require()` + the `node:*` built-ins. No-op without JSC. Must run
/// after `process` + the web globals (relies on `TextEncoder`/`TextDecoder`).
pub fn install(allocator: std.mem.Allocator, ctx: *JSContextRef, global: *JSGlobalObject) void {
    if (comptime !build_options.enable_jsc) return;

    callback.registerCallback(ctx, global, "__home_node_read", readNative);
    callback.registerCallback(ctx, global, "__home_node_write", writeNative);
    callback.registerCallback(ctx, global, "__home_node_stat", statNative);
    callback.registerCallback(ctx, global, "__home_node_mkdir", mkdirNative);

    const result = evaluate.evaluateUtf8Detailed(allocator, ctx, install_glue, "home:node-modules-install", 1) catch return;
    result.deinit(allocator);
}

fn evalBool(allocator: std.mem.Allocator, ctx: *JSContextRef, source: []const u8) !bool {
    const value = (try evaluate.evaluateUtf8(allocator, ctx, source, "home:node-probe", 1, null)) orelse
        return error.JSEvaluateReturnedNull;
    return extern_fns.JSValueToBoolean(ctx, value);
}

fn installRealm(allocator: std.mem.Allocator, ctx: *JSContextRef, global: *JSGlobalObject) void {
    @import("web_globals.zig").install(allocator, ctx, global);
    @import("process.zig").install(allocator, ctx, global, &[_][]const u8{"home"});
    install(allocator, ctx, global);
}

