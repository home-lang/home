/// `__home_node_hash(algo, dataUint8Array)` -> digest Uint8Array (or null for
/// an unsupported algorithm). One-shot; the JS side accumulates `update()`s.
fn hashNative(ctx: ?*JSContextRef, function: ?*JSObject, this_object: ?*JSObject, argc: usize, argv: [*c]const ?*JSValue, exception: extern_fns.ExceptionRef) callconv(.c) ?*JSValue {
    _ = function;
    _ = this_object;
    _ = exception;
    const c = ctx orelse return null;
    if (argc < 2) return extern_fns.JSValueMakeNull(c);
    const algo_v = argv[0] orelse return extern_fns.JSValueMakeNull(c);
    const data_v = argv[1] orelse return extern_fns.JSValueMakeNull(c);

    const allocator = std.heap.page_allocator;
    const algo = argToOwnedUtf8(c, algo_v, allocator) orelse return extern_fns.JSValueMakeNull(c);
    defer allocator.free(algo);

    if (extern_fns.JSValueGetTypedArrayType(c, data_v, null) == .kJSTypedArrayTypeNone)
        return extern_fns.JSValueMakeNull(c);
    const obj = extern_fns.JSValueToObject(c, data_v, null) orelse return extern_fns.JSValueMakeNull(c);
    const len = extern_fns.JSObjectGetTypedArrayByteLength(c, obj, null);
    const ptr = extern_fns.JSObjectGetTypedArrayBytesPtr(c, obj, null);
    const data: []const u8 = if (len > 0 and ptr != null) @as([*]const u8, @ptrCast(ptr.?))[0..len] else "";

    if (std.mem.eql(u8, algo, "sha256")) {
        var out: [32]u8 = undefined;
        std.crypto.hash.sha2.Sha256.hash(data, &out, .{});
        return makeUint8Array(c, &out);
    } else if (std.mem.eql(u8, algo, "sha512")) {
        var out: [64]u8 = undefined;
        std.crypto.hash.sha2.Sha512.hash(data, &out, .{});
        return makeUint8Array(c, &out);
    } else if (std.mem.eql(u8, algo, "sha1")) {
        var out: [20]u8 = undefined;
        std.crypto.hash.Sha1.hash(data, &out, .{});
        return makeUint8Array(c, &out);
    } else if (std.mem.eql(u8, algo, "md5")) {
        var out: [16]u8 = undefined;
        std.crypto.hash.Md5.hash(data, &out, .{});
        return makeUint8Array(c, &out);
    }
    return extern_fns.JSValueMakeNull(c);
}

/// `__home_node_random_bytes(n)` -> Uint8Array of `n` CSPRNG bytes.
fn randomBytesNative(ctx: ?*JSContextRef, function: ?*JSObject, this_object: ?*JSObject, argc: usize, argv: [*c]const ?*JSValue, exception: extern_fns.ExceptionRef) callconv(.c) ?*JSValue {
    _ = function;
    _ = this_object;
    _ = exception;
    const c = ctx orelse return null;
    var n: usize = 0;
    if (argc >= 1) {
        if (argv[0]) |v| {
            const f = extern_fns.JSValueToNumber(c, v, null);
            if (!std.math.isNan(f) and f > 0) n = @intFromFloat(@min(f, 16 * 1024 * 1024));
        }
    }
    if (n == 0) return makeUint8Array(c, "");
    const allocator = std.heap.page_allocator;
    const buf = allocator.alloc(u8, n) catch return makeUint8Array(c, "");
    defer allocator.free(buf);
    std.c.arc4random_buf(buf.ptr, n);
    return makeUint8Array(c, buf);
}

/// `__home_zlib(mode, dataUint8Array)` -> Uint8Array, or null on error.
/// mode: 0 gzip / 2 zlib / 4 raw deflate (compress); 1/3/5 the inverses.
fn zlibNative(ctx: ?*JSContextRef, function: ?*JSObject, this_object: ?*JSObject, argc: usize, argv: [*c]const ?*JSValue, exception: extern_fns.ExceptionRef) callconv(.c) ?*JSValue {
    _ = function;
    _ = this_object;
    _ = exception;
    const c = ctx orelse return null;
    if (argc < 2) return extern_fns.JSValueMakeNull(c);
    const mode_v = argv[0] orelse return extern_fns.JSValueMakeNull(c);
    const data_v = argv[1] orelse return extern_fns.JSValueMakeNull(c);
    const mode: u8 = @intFromFloat(extern_fns.JSValueToNumber(c, mode_v, null));

    if (extern_fns.JSValueGetTypedArrayType(c, data_v, null) == .kJSTypedArrayTypeNone) return extern_fns.JSValueMakeNull(c);
    const obj = extern_fns.JSValueToObject(c, data_v, null) orelse return extern_fns.JSValueMakeNull(c);
    const len = extern_fns.JSObjectGetTypedArrayByteLength(c, obj, null);
    const ptr = extern_fns.JSObjectGetTypedArrayBytesPtr(c, obj, null);
    const input: []const u8 = if (len > 0 and ptr != null) @as([*]const u8, @ptrCast(ptr.?))[0..len] else "";

    var arena_inst = std.heap.ArenaAllocator.init(std.heap.page_allocator);
    defer arena_inst.deinit();
    const a = arena_inst.allocator();

    const flate = std.compress.flate;
    const container: flate.Container = switch (mode) {
        0, 1 => .gzip,
        2, 3 => .zlib,
        4, 5 => .raw,
        else => return extern_fns.JSValueMakeNull(c),
    };
    const out: ?[]u8 = if (mode % 2 == 0)
        zlibCompress(a, input, container) catch null
    else
        zlibDecompress(a, input, container) catch null;
    if (out) |bytes| return makeUint8Array(c, bytes);
    return extern_fns.JSValueMakeNull(c);
}

fn zlibCompress(a: std.mem.Allocator, input: []const u8, container: std.compress.flate.Container) ![]u8 {
    const flate = std.compress.flate;
    var sink = try std.Io.Writer.Allocating.initCapacity(a, 4096);
    const work = try a.alloc(u8, flate.max_window_len);
    var comp = try flate.Compress.init(&sink.writer, work, container, flate.Compress.Options.default);
    try comp.writer.writeAll(input);
    try comp.finish();
    try sink.writer.flush();
    return sink.written();
}

fn zlibDecompress(a: std.mem.Allocator, input: []const u8, container: std.compress.flate.Container) ![]u8 {
    const flate = std.compress.flate;
    var reader: std.Io.Reader = .fixed(input);
    const window = try a.alloc(u8, flate.max_window_len);
    var dec = flate.Decompress.init(&reader, container, window);
    return try dec.reader.allocRemaining(a, std.Io.Limit.unlimited);
}

fn readTypedBytes(c: *JSContextRef, v: *JSValue) []const u8 {
    if (extern_fns.JSValueGetTypedArrayType(c, v, null) == .kJSTypedArrayTypeNone) return "";
    const obj = extern_fns.JSValueToObject(c, v, null) orelse return "";
    const len = extern_fns.JSObjectGetTypedArrayByteLength(c, obj, null);
    const ptr = extern_fns.JSObjectGetTypedArrayBytesPtr(c, obj, null);
    return if (len > 0 and ptr != null) @as([*]const u8, @ptrCast(ptr.?))[0..len] else "";
}

fn pbkdf2Run(dk: []u8, pw: []const u8, salt: []const u8, iters: u32, digest: []const u8) bool {
    const hmac = std.crypto.auth.hmac;
    if (std.mem.eql(u8, digest, "sha256")) std.crypto.pwhash.pbkdf2(dk, pw, salt, iters, hmac.sha2.HmacSha256) catch return false
    else if (std.mem.eql(u8, digest, "sha512")) std.crypto.pwhash.pbkdf2(dk, pw, salt, iters, hmac.sha2.HmacSha512) catch return false
    else if (std.mem.eql(u8, digest, "sha1")) std.crypto.pwhash.pbkdf2(dk, pw, salt, iters, hmac.HmacSha1) catch return false
    else return false;
    return true;
}

fn argToOwnedUtf8Lower(ctx: *JSContextRef, value: *JSValue, allocator: std.mem.Allocator) ?[]u8 {
    const string = extern_fns.JSValueToStringCopy(ctx, value, null) orelse return null;
    defer extern_fns.JSStringRelease(string);
    const capacity = extern_fns.JSStringGetLength(string) * 4 + 1;
    const buf = allocator.alloc(u8, capacity) catch return null;
    const written = extern_fns.JSStringGetUTF8CString(string, buf.ptr, buf.len);
    const s = buf[0 .. if (written > 0) written - 1 else 0];
    for (s) |*ch| ch.* = std.ascii.toLower(ch.*);
    return s;
}

fn hkdfRun(out: []u8, digest: []const u8, ikm: []const u8, salt: []const u8, info: []const u8) bool {
    const k = std.crypto.kdf.hkdf;
    if (std.mem.eql(u8, digest, "sha256")) {
        const prk = k.HkdfSha256.extract(salt, ikm);
        k.HkdfSha256.expand(out, info, prk);
    } else if (std.mem.eql(u8, digest, "sha512")) {
        const prk = k.HkdfSha512.extract(salt, ikm);
        k.HkdfSha512.expand(out, info, prk);
    } else return false;
    return true;
}

/// `__home_hkdf(digest, ikmBytes, saltBytes, infoBytes, keylen)` -> Uint8Array or null.
fn hkdfNative(ctx: ?*JSContextRef, function: ?*JSObject, this_object: ?*JSObject, argc: usize, argv: [*c]const ?*JSValue, exception: extern_fns.ExceptionRef) callconv(.c) ?*JSValue {
    _ = function;
    _ = this_object;
    _ = exception;
    const c = ctx orelse return null;
    if (argc < 5) return extern_fns.JSValueMakeNull(c);
    const ikm = readTypedBytes(c, argv[1].?);
    const salt = readTypedBytes(c, argv[2].?);
    const info = readTypedBytes(c, argv[3].?);
    const keylen_f = extern_fns.JSValueToNumber(c, argv[4].?, null);
    if (!(keylen_f >= 1) or keylen_f > 8160) return extern_fns.JSValueMakeNull(c);
    const keylen: usize = @intFromFloat(keylen_f);
    var arena_inst = std.heap.ArenaAllocator.init(std.heap.page_allocator);
    defer arena_inst.deinit();
    const a = arena_inst.allocator();
    const digest = argToOwnedUtf8Lower(c, argv[0].?, a) orelse return extern_fns.JSValueMakeNull(c);
    const out = a.alloc(u8, keylen) catch return extern_fns.JSValueMakeNull(c);
    if (!hkdfRun(out, digest, ikm, salt, info)) return extern_fns.JSValueMakeNull(c);
    return makeUint8Array(c, out);
}

/// `__home_scrypt(pwBytes, saltBytes, keylen, N, r, p)` -> Uint8Array or null.
